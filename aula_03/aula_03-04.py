# Entrada dos dados
n1 = int(input('Informe o primeiro valor: '))
n2 = int(input('Informe o segundo valor: '))

# Processamento dos dados
if n1 == n2:
    print(f'Os valores são iguais, portanto sua soma vale {n1+n2}')
else:
    print(f'Eles são diferentes, portanto seu produto vale {n1*n2}')